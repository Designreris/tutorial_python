# Variable - Use a lowercase single letter, word, or words. Separate words with underscores to improve readability.	
# x, var, python_variable

# Constant - Use an uppercase single letter, word, or words. Separate words with underscores to improve readability.	
# CONSTANT, PYTHON_CONSTANT, PYTHON_LONG_CONSTANT

# Function - Use a lowercase word or words. Separate words by underscores to improve readability. This style is called snake case.	
# function, python_function

# Method - Use a lowercase word or words. Separate words with underscores to improve readability (snake case).	
# class_method, method

# Class -	Start each word with a capital letter. Don’t separate words with underscores. This style is called camel case or Pascal case.	
# Model, PythonClass

# Module - Use a short, lowercase word or words. Separate words with underscores to improve readability.	
# module.py, python_module.py

# Package -	Use a short, lowercase word or words. Don’t separate words with underscores.	
# package, pythonpackage
